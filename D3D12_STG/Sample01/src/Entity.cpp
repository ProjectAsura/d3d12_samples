#include "Entity.h"
#include "SpriteData.h"
#include <gfx/asdxSprite.h>


///////////////////////////////////////////////////////////////////////////////
// Entity class
///////////////////////////////////////////////////////////////////////////////

//-----------------------------------------------------------------------------
//      コンストラクタです.
//-----------------------------------------------------------------------------
Entity::Entity()
{ /* DO_NOTHING */ }

//-----------------------------------------------------------------------------
//      引数付きコンストラクタです.
//-----------------------------------------------------------------------------
Entity::Entity(uint16_t kind, float x, float y)
: m_Kind(kind)
, m_Pos(x, y)
{ /* DO_NOTHING */ }

//-----------------------------------------------------------------------------
//      引数付きコンストラクタです.
//-----------------------------------------------------------------------------
Entity::Entity(uint16_t kind, const asdx::Vector2& pos)
: m_Kind(kind)
, m_Pos(pos)
{ /* DO_NOTHING */ }

//-----------------------------------------------------------------------------
//      デストラクタです.
//-----------------------------------------------------------------------------
Entity::~Entity()
{ /* DO_NOTHING */ }

//-----------------------------------------------------------------------------
//      スプライト種別を設定します.
//-----------------------------------------------------------------------------
void Entity::SetKind(uint16_t value)
{ m_Kind = value; }

//-----------------------------------------------------------------------------
//      位置座標を設定します.
//-----------------------------------------------------------------------------
void Entity::SetPos(const asdx::Vector2& value)
{ m_Pos = value; }

//-----------------------------------------------------------------------------
//      位置座標を設定します.
//-----------------------------------------------------------------------------
void Entity::SetPos(float x, float y)
{
    m_Pos.x = x;
    m_Pos.y = y;
}

//-----------------------------------------------------------------------------
//      位置座標のX成分を設定します.
//-----------------------------------------------------------------------------
void Entity::SetPosX(float value)
{ m_Pos.x = value; }

//-----------------------------------------------------------------------------
//      位置座標のY成分を設定します.
//-----------------------------------------------------------------------------
void Entity::SetPosY(float value)
{ m_Pos.y = value; }

//-----------------------------------------------------------------------------
//      スプライト種別を取得します.
//-----------------------------------------------------------------------------
uint16_t Entity::GetKind() const
{ return m_Kind; }

//-----------------------------------------------------------------------------
//      位置座標を設定します.
//-----------------------------------------------------------------------------
const asdx::Vector2& Entity::GetPos() const
{ return m_Pos; }

//-----------------------------------------------------------------------------
//      位置座標のX成分を取得します.
//-----------------------------------------------------------------------------
float Entity::GetPosX() const
{ return m_Pos.x; }

//-----------------------------------------------------------------------------
//      位置座標のY成分を取得します.
//-----------------------------------------------------------------------------
float Entity::GetPosY() const
{ return m_Pos.y; }

//-----------------------------------------------------------------------------
//      スプライトサイズを取得します.
//-----------------------------------------------------------------------------
asdx::Int2 Entity::GetSize() const
{
    const auto& data = GetSpriteData(SpriteKind(m_Kind));
    return asdx::Int2(data.W, data.H);
}

//-----------------------------------------------------------------------------
//      スプライトを描画します.
//-----------------------------------------------------------------------------
void Entity::Draw(asdx::SpriteRenderer& renderer)
{
    const auto& data = GetSpriteData(SpriteKind(m_Kind));
    renderer.Add(int(m_Pos.x), int(m_Pos.y), data.W, data.H, data.uv0, data.uv1);
}

//-----------------------------------------------------------------------------
//      交差判定を行います.
//-----------------------------------------------------------------------------
bool Entity::IsHit(const asdx::Vector2& pos, const asdx::Int2& size)
{
    const auto& data = GetSpriteData(SpriteKind(m_Kind));
    return !((m_Pos.x + float(data.W)) <= pos.x || (pos.x + float(size.x)) <= m_Pos.x ||
             (m_Pos.y + float(data.H)) <= pos.y || (pos.y + float(size.y)) <= m_Pos.y);
}

//-----------------------------------------------------------------------------
//      画面外かどうか判定します.
//-----------------------------------------------------------------------------
bool Entity::IsOutOfScreen(uint32_t w, uint32_t h)
{
    const auto& data = GetSpriteData(SpriteKind(m_Kind));
    if ((m_Pos.x + float(data.W)) < 0 || m_Pos.x > float(w)
     || (m_Pos.y + float(data.H)) < 0 || m_Pos.y > float(h))
    { return true; }

    return false;
}