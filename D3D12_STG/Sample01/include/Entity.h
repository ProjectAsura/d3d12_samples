#pragma once

//-----------------------------------------------------------------------------
// Includes
//-----------------------------------------------------------------------------
#include <fnd/asdxMath.h>

//-----------------------------------------------------------------------------
// Forward Decalarations.
//-----------------------------------------------------------------------------
namespace asdx { class SpriteRenderer; }


///////////////////////////////////////////////////////////////////////////////
// Entity class
///////////////////////////////////////////////////////////////////////////////
class Entity
{
    //=========================================================================
    // list of friend classes and methods.
    //=========================================================================
    /* NOTHING */

public:
    //=========================================================================
    // public variables.
    //=========================================================================
    /* NOTHING */

    //=========================================================================
    // public methods.
    //=========================================================================

    //-------------------------------------------------------------------------
    //! @brief      コンストラクタです.
    //-------------------------------------------------------------------------
    Entity();

    //-------------------------------------------------------------------------
    //! @brief      引数付きコンストラクタです.
    //! 
    //! @param[in]      kind        スプライト種別.
    //! @param[in]      x           位置X成分.
    //! @param[in]      y           位置Y成分.
    //-------------------------------------------------------------------------
    Entity(uint16_t kind, float x, float y);

    //-------------------------------------------------------------------------
    //! @brief      引数付きコンストラクタです.
    //! 
    //! @param[in]      kind        スプライト種別.
    //! @param[in]      pos         位置座標.
    //-------------------------------------------------------------------------
    Entity(uint16_t kind, const asdx::Vector2& pos);

    //-------------------------------------------------------------------------
    //! @brief      デストラクタです.
    //-------------------------------------------------------------------------
    ~Entity();

    //-------------------------------------------------------------------------
    //! @brief      スプライト種別を設定します.
    //! 
    //! @param[in]      value        スプライト種別です.
    //-------------------------------------------------------------------------
    void SetKind(uint16_t value);

    //-------------------------------------------------------------------------
    //! @brief      位置座標を設定します.
    //! 
    //! @param[in]      value       設定する位置座標です.
    //-------------------------------------------------------------------------
    void SetPos(const asdx::Vector2& value);

    //-------------------------------------------------------------------------
    //! @brief      位置座標を設定します.
    //! 
    //! @param[in]      x           位置X成分.
    //! @param[in]      y           位置Y成分.
    //-------------------------------------------------------------------------
    void SetPos(float x, float y);

    //-------------------------------------------------------------------------
    //! @brief      位置座標のX成分を設定します.
    //! 
    //! @param[in]      value       設定する値.
    //-------------------------------------------------------------------------
    void SetPosX(float value);

    //-------------------------------------------------------------------------
    //! @brief      位置座標のY成分を設定します.
    //! 
    //! @param[in]      value       設定する値.
    //-------------------------------------------------------------------------
    void SetPosY(float value);

    //-------------------------------------------------------------------------
    //! @brief      スプライト種別を取得します.
    //! 
    //! @return     スプライト種別を返却します.
    //-------------------------------------------------------------------------
    uint16_t GetKind() const;

    //-------------------------------------------------------------------------
    //! @brief      位置座標を取得します.
    //! 
    //! @return     位置座標を返却します.
    //-------------------------------------------------------------------------
    const asdx::Vector2& GetPos() const;

    //-------------------------------------------------------------------------
    //! @brief      位置座標のX成分を取得します.
    //! 
    //! @return     位置座標のX成分を返却します.
    //-------------------------------------------------------------------------
    float GetPosX() const;

    //-------------------------------------------------------------------------
    //! @brief      位置座標のY成分を取得します.
    //! 
    //! @return     位置座標のY成分を返却します.
    //-------------------------------------------------------------------------
    float GetPosY() const;

    //-------------------------------------------------------------------------
    //! @brief      スプライトサイズを取得します.
    //! 
    //! @return     スプライトサイズを返却します.
    //-------------------------------------------------------------------------
    asdx::Int2 GetSize() const;

    //-------------------------------------------------------------------------
    //! @brief      スプライトを描画します.
    //! 
    //! @param[in]      renderer        スプライトレンダラー.
    //-------------------------------------------------------------------------
    void Draw(asdx::SpriteRenderer& renderer);

    //-------------------------------------------------------------------------
    //! @brief      交差判定を行います.
    //! 
    //! @param[in]      pos     対象の位置座標.
    //! @param[in]      size    対象のスプライトサイズ.
    //! @retval true    交差あり.
    //! @retval false   交差なし.
    //-------------------------------------------------------------------------
    bool IsHit(const asdx::Vector2& pos, const asdx::Int2& size);

    //-------------------------------------------------------------------------
    //! @brief      スクリーン外であるかどうか判定します.
    //! 
    //! @param[in]      w       スクリーンの横幅.
    //! @param[in]      h       スクリーンの縦幅.
    //-------------------------------------------------------------------------
    bool IsOutOfScreen(uint32_t w, uint32_t h);

private:
    //=========================================================================
    // private variables.
    //=========================================================================
    uint16_t        m_Kind = 0;                         //!< スプライト種別.
    asdx::Vector2   m_Pos  = asdx::Vector2(0.0f, 0.0f); //!< 位置座標.

    //=========================================================================
    // private methods.
    //=========================================================================
    /* NOTHING */
};
