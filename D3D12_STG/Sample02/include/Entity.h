#pragma once

//-----------------------------------------------------------------------------
// Includes
//-----------------------------------------------------------------------------
#include <fnd/asdxMath.h>

//-----------------------------------------------------------------------------
// Forward Decalarations.
//-----------------------------------------------------------------------------
namespace asdx { class SpriteRenderer; }


///////////////////////////////////////////////////////////////////////////////
// Entity class
///////////////////////////////////////////////////////////////////////////////
class Entity
{
    //=========================================================================
    // list of friend classes and methods.
    //=========================================================================
    /* NOTHING */

public:
    //=========================================================================
    // public variables.
    //=========================================================================
    /* NOTHING */

    //=========================================================================
    // public methods.
    //=========================================================================

    //-------------------------------------------------------------------------
    //! @brief      コンストラクタです.
    //-------------------------------------------------------------------------
    Entity();

    //-------------------------------------------------------------------------
    //! @brief      引数付きコンストラクタです.
    //! 
    //! @param[in]      kind        スプライト種別.
    //! @param[in]      x           位置X成分.
    //! @param[in]      y           位置Y成分.
    //! @param[in]      center      中心座標として設定する場合は true を指定.
    //-------------------------------------------------------------------------
    Entity(uint16_t kind, float x, float y, bool center);

    //-------------------------------------------------------------------------
    //! @brief      引数付きコンストラクタです.
    //! 
    //! @param[in]      kind        スプライト種別.
    //! @param[in]      pos         位置座標.
    //! @param[in]      center      中心座標として設定する場合は true を指定.
    //-------------------------------------------------------------------------
    Entity(uint16_t kind, const asdx::Vector2& pos, bool center);

    //-------------------------------------------------------------------------
    //! @brief      引数付きコンストラクタです.
    //! 
    //! @param[in]      kind        スプライト種別.
    //! @param[in]      x           位置X成分.
    //! @param[in]      y           位置Y成分.
    //! @param[in]      sx          X方向の描画スケール.
    //! @param[in]      sy          Y方向の描画スケール.
    //! @param[in]      center      中心座標として設定する場合は true を指定.
    //-------------------------------------------------------------------------
    Entity(uint16_t kind, float x, float y, float sx, float sy, bool center);

    //-------------------------------------------------------------------------
    //! @brief      引数付きコンストラクタです.
    //! 
    //! @param[in]      kind        スプライト種別.
    //! @param[in]      pos         位置座標.
    //! @param[in]      scale       びょぐあスケール.
    //! @param[in]      center      中心座標として設定する場合は true を指定.
    //-------------------------------------------------------------------------
    Entity(uint16_t kind, const asdx::Vector2& pos, const asdx::Vector2& scale, bool center);

    //-------------------------------------------------------------------------
    //! @brief      デストラクタです.
    //-------------------------------------------------------------------------
    ~Entity();

    //-------------------------------------------------------------------------
    //! @brief      スプライト種別を設定します.
    //! 
    //! @param[in]      value        スプライト種別です.
    //-------------------------------------------------------------------------
    void SetKind(uint16_t value);

    //-------------------------------------------------------------------------
    //! @brief      位置座標を設定します.
    //! 
    //! @param[in]      value       設定する位置座標です.
    //! @note       左上の座標を設定します.
    //-------------------------------------------------------------------------
    void SetPos(const asdx::Vector2& value);

    //-------------------------------------------------------------------------
    //! @brief      位置座標を設定します.
    //! 
    //! @param[in]      x           位置X成分.
    //! @param[in]      y           位置Y成分.
    //! @note       左上の座標を設定します.
    //-------------------------------------------------------------------------
    void SetPos(float x, float y);

    //-------------------------------------------------------------------------
    //! @brief      位置座標のX成分を設定します.
    //! 
    //! @param[in]      value       設定する値.
    //-------------------------------------------------------------------------
    void SetPosX(float value);

    //-------------------------------------------------------------------------
    //! @brief      位置座標のY成分を設定します.
    //! 
    //! @param[in]      value       設定する値.
    //-------------------------------------------------------------------------
    void SetPosY(float value);

    //-------------------------------------------------------------------------
    //! @brief      指定された点が中心になるように位置座標を設定します.
    //! 
    //! @param[in]      value       設定する中心座標.
    //-------------------------------------------------------------------------
    void SetCenter(const asdx::Vector2& value);

    //-------------------------------------------------------------------------
    //! @brief      指定された点が中心になるように位置座標を設定します.
    //! 
    //! @param[in]      x           位置X成分.
    //! @param[in]      y           位置Y成分.
    //-------------------------------------------------------------------------
    void SetCenter(float x, float y);

    //-------------------------------------------------------------------------
    //! @brief      指定された点がX方向の中心になるように位置座標を設定します.
    //! 
    //! @param[in]      value       設定する値.
    //-------------------------------------------------------------------------
    void SetCenterX(float value);

    //-------------------------------------------------------------------------
    //! @brief      指定された点がY方向の中心になるように位置座標を設定します.
    //! 
    //! @param[in]      value       設定する値.
    //-------------------------------------------------------------------------
    void SetCenterY(float value);

    //-------------------------------------------------------------------------
    //! @brief      描画スケールを設定します.
    //! 
    //! @param[in]      value       設定するスケール.
    //-------------------------------------------------------------------------
    void SetScale(const asdx::Vector2& value);

    //-------------------------------------------------------------------------
    //! @brief      描画スケールを設定します.
    //! 
    //! @param[in]      x           設定する横幅スケール.
    //! @param[in]      y           設定する縦幅スケール.
    //-------------------------------------------------------------------------
    void SetScale(float x, float y);

    //-------------------------------------------------------------------------
    //! @brief      X方向の描画スケールを設定します.
    //! 
    //! @param[in]      value       設定するスケール.
    //-------------------------------------------------------------------------
    void SetScaleX(float value);

    //-------------------------------------------------------------------------
    //! @brief      Y方向の描画スケールを設定します.
    //! 
    //! @param[in]      value       設定するスケール.
    //-------------------------------------------------------------------------
    void SetScaleY(float value);

    //-------------------------------------------------------------------------
    //! @brief      スプライト種別を取得します.
    //! 
    //! @return     スプライト種別を返却します.
    //-------------------------------------------------------------------------
    uint16_t GetKind() const;

    //-------------------------------------------------------------------------
    //! @brief      位置座標を取得します.
    //! 
    //! @return     位置座標を返却します.
    //! @note       左上の座標を返却します.
    //-------------------------------------------------------------------------
    const asdx::Vector2& GetPos() const;

    //-------------------------------------------------------------------------
    //! @brief      位置座標のX成分を取得します.
    //! 
    //! @return     位置座標のX成分を返却します.
    //-------------------------------------------------------------------------
    float GetPosX() const;

    //-------------------------------------------------------------------------
    //! @brief      位置座標のY成分を取得します.
    //! 
    //! @return     位置座標のY成分を返却します.
    //-------------------------------------------------------------------------
    float GetPosY() const;

    //-------------------------------------------------------------------------
    //! @brief      中心座標を取得します.
    //! 
    //! @return     中心座標を返却します.
    //-------------------------------------------------------------------------
    asdx::Vector2 GetCenter() const;

    //-------------------------------------------------------------------------
    //! @brief      中心座標のX成分を取得します.
    //! 
    //! @return     中心座標のX成分を返却します.
    //-------------------------------------------------------------------------
    float GetCenterX() const;

    //-------------------------------------------------------------------------
    //! @brief      中心座標のX成分を取得します.
    //! 
    //! @return     中心座標のX成分を返却します.
    //-------------------------------------------------------------------------
    float GetCenterY() const;

    //-------------------------------------------------------------------------
    //! @brief      スプライトサイズを取得します.
    //! 
    //! @return     スプライトサイズを返却します.
    //-------------------------------------------------------------------------
    asdx::Int2 GetSize() const;

    //-------------------------------------------------------------------------
    //! @brief      スケール済みスプライトサイズを取得します.
    //! 
    //! @return     スケール済みスプライトサイズを取得します.
    //-------------------------------------------------------------------------
    asdx::Int2 GetScaledSize() const;

    //-------------------------------------------------------------------------
    //! @brief      描画スケールを取得します.
    //! 
    //! @return     描画スケールを返却します
    //-------------------------------------------------------------------------
    const asdx::Vector2& GetScale() const;

    //-------------------------------------------------------------------------
    //! @brief      X方向の描画スケールを取得します
    //! 
    //! @return     X方向の描画スケールを返却します.
    //-------------------------------------------------------------------------
    float GetScaleX() const;

    //-------------------------------------------------------------------------
    //! @brief      Y方向の描画スケールを取得します.
    //! 
    //! @return     Y方向の描画スケールを返却します.
    //-------------------------------------------------------------------------
    float GetScaleY() const;

    //-------------------------------------------------------------------------
    //! @brief      スプライトを描画します.
    //! 
    //! @param[in]      renderer    スプライトレンダラー.
    //-------------------------------------------------------------------------
    void Draw(asdx::SpriteRenderer& renderer);

    //-------------------------------------------------------------------------
    //! @brief      交差判定を行います.
    //! 
    //! @param[in]      pos     対象の位置座標.
    //! @param[in]      size    対象のスプライトサイズ.
    //! @retval true    交差あり.
    //! @retval false   交差なし.
    //-------------------------------------------------------------------------
    bool IsHit(const asdx::Vector2& pos, const asdx::Int2& size) const;

    //-------------------------------------------------------------------------
    //! @brief      交差判定を行います
    //! 
    //! @param[in]      target      対象エンティティ.
    //-------------------------------------------------------------------------
    bool IsHit(const Entity& target) const;

    //-------------------------------------------------------------------------
    //! @brief      スクリーン外であるかどうか判定します.
    //! 
    //! @param[in]      w       スクリーンの横幅.
    //! @param[in]      h       スクリーンの縦幅.
    //-------------------------------------------------------------------------
    bool IsOutOfScreen(uint32_t w, uint32_t h);

    //-------------------------------------------------------------------------
    //! @brief      指定ターゲットへの角度を求めます.
    //! 
    //! @param[in]      targetPos       ターゲット位置.
    //-------------------------------------------------------------------------
    float ToAngle(const asdx::Vector2& targetPos);

private:
    //=========================================================================
    // private variables.
    //=========================================================================
    uint16_t        m_Kind  = 0;                         //!< スプライト種別.
    asdx::Vector2   m_Pos   = asdx::Vector2(0.0f, 0.0f); //!< 位置座標.
    asdx::Vector2   m_Scale = asdx::Vector2(1.0f, 1.0f); //!< 描画スケール

    //=========================================================================
    // private methods.
    //=========================================================================
    /* NOTHING */
};
