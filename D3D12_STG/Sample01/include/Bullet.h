///////////////////////////////////////////////////////////////////////////////
// Bullet class
///////////////////////////////////////////////////////////////////////////////
class Bullet : public Entity
{
    //=========================================================================
    // list of friend classes and methods.
    //=========================================================================
    /* NOTHING */

public:
    //=========================================================================
    // public variables.
    //=========================================================================
    /* NOTHING */

    //=========================================================================
    // public methods.
    //=========================================================================

    //-------------------------------------------------------------------------
    //! @brief      コンストラクタです.
    //-------------------------------------------------------------------------
    Bullet();

    //-------------------------------------------------------------------------
    //! @brief      引数付きコンストラクタです.
    //-------------------------------------------------------------------------
    Bullet(uint16_t kind, float px, float py, float vx, float vy);

    //-------------------------------------------------------------------------
    //! @brief      引数付きコンストラクタです.
    //-------------------------------------------------------------------------
    Bullet(uint16_t kind, const asdx::Vector2& pos, const asdx::Vector2& vel);

    //-------------------------------------------------------------------------
    //! @brief      速度ベクトルを設定します.
    //! 
    //! @param[in]      value       設定する値.
    //-------------------------------------------------------------------------
    void SetVel(const asdx::Vector2& value);

    //-------------------------------------------------------------------------
    //! @brief      速度ベクトルを設定します.
    //! 
    //! @param[in]      x           速度ベクトルのX成分.
    //! @param[in]      y           速度ベクトルのY成分.
    //-------------------------------------------------------------------------
    void SetVel(float x, float y);

    //-------------------------------------------------------------------------
    //! @brief      速度ベクトルのX成分を設定します.
    //! 
    //! @param[in]      value       設定する値.
    //-------------------------------------------------------------------------
    void SetVelX(float value);

    //-------------------------------------------------------------------------
    //! @brief      速度ベクトルのY成分を設定します.
    //! 
    //! @param[in]      value       設定する値.
    //-------------------------------------------------------------------------
    void SetVelY(float value);

    //-------------------------------------------------------------------------
    //! @brief      速度ベクトルを取得します.
    //! 
    //! @return     速度ベクトルを返却します.
    //-------------------------------------------------------------------------
    const asdx::Vector2& GetVel() const;

    //-------------------------------------------------------------------------
    //! @brief      速度ベクトルのX成分を取得します.
    //! 
    //! @return     速度ベクトルのX成分を返却します.
    //-------------------------------------------------------------------------
    float GetVelX() const;

    //-------------------------------------------------------------------------
    //! @brief      速度ベクトルのX成分を取得します.
    //! 
    //! @return     速度ベクトルのY成分を返却します.
    //-------------------------------------------------------------------------
    float GetVelY() const;

    //-------------------------------------------------------------------------
    //! @brief      更新処理を行います.
    //-------------------------------------------------------------------------
    void Update();

private:
    //=========================================================================
    // private variables.
    //=========================================================================
    asdx::Vector2 m_Vel = asdx::Vector2(0.0f, 0.0f);    //!< 速度ベクトル.

    //=========================================================================
    // private methods.
    //=========================================================================
    /* NOTHING */
};
