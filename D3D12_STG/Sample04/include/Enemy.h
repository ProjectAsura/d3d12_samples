///////////////////////////////////////////////////////////////////////////////
// IBehavior interface
///////////////////////////////////////////////////////////////////////////////
struct IBehavior
{
    //-------------------------------------------------------------------------
    //! @brief      デストラクタです.
    //-------------------------------------------------------------------------
    virtual ~IBehavior() { /* DO_NOTHING */ }

    //-------------------------------------------------------------------------
    //! @brief      毎フレーム行う処理です.
    //! 
    //! @param[in]      entity      更新対象のエンティティです.
    //-------------------------------------------------------------------------
    virtual void OnTick(Enemy& entity) = 0;
};


///////////////////////////////////////////////////////////////////////////////
// Enemy class
///////////////////////////////////////////////////////////////////////////////
class Enemy : public Entity, public asdx::List<Enemy>::Node
{
    //=========================================================================
    // list of friend classes and methods.
    //=========================================================================
    /* NOTHING */

public:
    //=========================================================================
    // public variables.
    //=========================================================================
    /* NOTHING */

    //=========================================================================
    // public methods.
    //=========================================================================

    //-------------------------------------------------------------------------
    //! @brief      コンストラクタです.
    //-------------------------------------------------------------------------
    Enemy();

    //-------------------------------------------------------------------------
    //! @brief      デストラクタです.
    //-------------------------------------------------------------------------
    ~Enemy();

    //-------------------------------------------------------------------------
    //! @brief      セットアップ処理を行います.
    //! 
    //! @param[in]      kind                スプライト種別.
    //! @param[in]      x                   位置X成分.
    //! @param[in]      y                   位置Y成分.
    //! @param[in]      shotBehavior        発弾挙動です.
    //! @param[in]      moveBehavior        移動挙動です.
    //-------------------------------------------------------------------------
    void Setup(
        uint16_t            kind,
        float               x,
        float               y,
        float               sx,
        float               sy,
        IBehavior*          pShotBehavior,
        IBehavior*          pMoveBehavior);

    //-------------------------------------------------------------------------
    //! @brief      更新処理を行います.
    //-------------------------------------------------------------------------
    void Update();

    //-------------------------------------------------------------------------
    //! @brief      パラメータを設定します.
    //! 
    //! @param[in]      value       設定するパラメータ.
    //-------------------------------------------------------------------------
    void SetParam(const asdx::Vector4& value);

    //-------------------------------------------------------------------------
    //! @brief      パラメータを取得します.
    //! 
    //! @return     パラメータを返却します.
    //-------------------------------------------------------------------------
    const asdx::Vector4& GetParam() const;

    //-------------------------------------------------------------------------
    //! @brief      タイマーを取得します.
    //! 
    //! @return     タイマーを返却します. (単位:フレーム数).
    //-------------------------------------------------------------------------
    int GetTimer() const;

private:
    //=========================================================================
    // private variables.
    //=========================================================================
    asdx::Vector4   m_Param         = {};
    int             m_Timer         = 0;
    IBehavior*      m_pShotBehavior = nullptr;
    IBehavior*      m_pMoveBehavior = nullptr;

    //=========================================================================
    // private methods.
    //=========================================================================
    /* NOTHING */
};


///////////////////////////////////////////////////////////////////////////////
// EnemyManager class
///////////////////////////////////////////////////////////////////////////////
class EnemyManager
{
    //=========================================================================
    // list of friend classes and methods.
    //=========================================================================
    /* NOTHING */

public:
    ///////////////////////////////////////////////////////////////////////////
    // SpawnParam structure
    ///////////////////////////////////////////////////////////////////////////
    struct SpawnParam
    {
        uint16_t    SpriteKind    = 0;         //!< スプライト種別.
        IBehavior*  pShotBehavior = nullptr;   //!< 発弾挙動.
        IBehavior*  pMoveBehavior = nullptr;   //!< 移動挙動.
    };

    //=========================================================================
    // public variables.
    //=========================================================================
    /* NOTHING */

    //=========================================================================
    // public methods.
    //=========================================================================

    //-------------------------------------------------------------------------
    //! @brief      コンストラクタです.
    //-------------------------------------------------------------------------
    EnemyManager();

    //-------------------------------------------------------------------------
    //! @brief      デストラクタです.
    //-------------------------------------------------------------------------
    ~EnemyManager();

    //-------------------------------------------------------------------------
    //! @brief      初期化処理を行います.
    //! 
    //! @param[in]      count       最大敵数.
    //! @retval true    初期化に成功.
    //! @retval false   初期化に失敗.
    //-------------------------------------------------------------------------
    bool Init(uint32_t count);

    //-------------------------------------------------------------------------
    //! @brief      終了処理を行います.
    //-------------------------------------------------------------------------
    void Term();

    //-------------------------------------------------------------------------
    //! @brief      生成タイプを登録します.
    //! 
    //! @param[in]      type        タイプ.
    //! @param[in]      param       生成パラメータ.
    //-------------------------------------------------------------------------
    void AddType(uint32_t type, SpawnParam param);

    //-------------------------------------------------------------------------
    //! @brief      生成タイプを登録解除します.
    //! 
    //! @param[in]      type        タイプ.
    //-------------------------------------------------------------------------
    void DelType(uint32_t type);

    //-------------------------------------------------------------------------
    //! @brief      生成タイプをクリアします.
    //-------------------------------------------------------------------------
    void ClearTypes();

    //-------------------------------------------------------------------------
    //! @brief      敵を生成します.
    //! 
    //! @param[in]      type        敵タイプ.
    //! @param[in]      x           位置X成分.
    //! @param[in]      y           位置Y成分.
    //! @param[in]      sx          描画スケールX.
    //! @param[in]      sy          描画スケールY.
    //! @retval true    生成に成功.
    //! @retval false   生成に失敗.
    //-------------------------------------------------------------------------
    bool Spwan(uint32_t type, float x, float y, float sx = 1.0f, float sy = 1.0f);

    //-------------------------------------------------------------------------
    //! @brief      更新処理を行います.
    //-------------------------------------------------------------------------
    void Update(uint32_t w, uint32_t h);

    //-------------------------------------------------------------------------
    //! @brief      スプライトを描画します.
    //-------------------------------------------------------------------------
    void Draw(asdx::SpriteRenderer& renderer);

    //-------------------------------------------------------------------------
    //! @brief      交差判定を行います.
    //! 
    //! @param[in]      entry       対象エンティティ.
    //! @retval true    交差あり.
    //! @retval false   交差なし.
    //-------------------------------------------------------------------------
    bool IsHit(const Entity& entity);

    //-------------------------------------------------------------------------
    //! @brief      交差判定を行います.
    //! 
    //! @param[in]      manager     弾マネージャ.
    //-------------------------------------------------------------------------
    void CheckHit(BulletManager& manager);

    //-------------------------------------------------------------------------
    //! @brief      生存している敵の数を取得します.
    //! 
    //! @return     生存している敵の数を返却します.
    //-------------------------------------------------------------------------
    uint32_t GetUsedCount() const;

    //-------------------------------------------------------------------------
    //! @brief      生成可能な敵の数の取得します.
    //! 
    //! @return     生成可能な敵の数を返却します.
    //-------------------------------------------------------------------------
    uint32_t GetFreeCount() const;

    //-------------------------------------------------------------------------
    //! @brief      最大数を取得します.
    //! 
    //! @return     最大数を返却します.
    //-------------------------------------------------------------------------
    uint32_t GetMaxCount() const;

    //-------------------------------------------------------------------------
    //! @brief      指定されたアクションを全要素に対して実行します.
    //! 
    //! @param[in]      action      アクション.
    //-------------------------------------------------------------------------
    template<typename Action>
    void ForEach(Action action)
    {
        for(auto& itr : m_UsedList)
        { action(itr); }
    }

private:
    //=========================================================================
    // private variables.
    //=========================================================================
    Enemy*              m_Enemies   = nullptr;  //!< 敵.
    uint32_t            m_MaxCount  = 0;        //!< 最大弾数.
    uint32_t            m_UsedCount = 0;        //!< 使用数.
    asdx::List<Enemy>   m_FreeList;             //!< 未使用リスト.
    asdx::List<Enemy>   m_UsedList;             //!< 使用中リスト.

    std::unordered_map<uint32_t, SpawnParam> m_Types;   //!< 敵タイプリスト.

    //=========================================================================
    // private methods.
    //=========================================================================
    /* NOTHING */
};

