#pragma once

//-----------------------------------------------------------------------------
// Includes
//-----------------------------------------------------------------------------
#include "Entity.h"
#include <fnd/asdxHid.h>


///////////////////////////////////////////////////////////////////////////////
// Player class
///////////////////////////////////////////////////////////////////////////////
class Player final : public Entity
{
    //=========================================================================
    // list of friend classes and methods.
    //=========================================================================
    /* NOTHING */

public:
    //=========================================================================
    // public variables.
    //=========================================================================
    /* NOTHING */

    //=========================================================================
    // public methods.
    //=========================================================================

    //-------------------------------------------------------------------------
    //! @brief      コンストラクタです.
    //-------------------------------------------------------------------------
    Player();

    //-------------------------------------------------------------------------
    //! @brief      デストラクタです.
    //-------------------------------------------------------------------------
    ~Player();

    //-------------------------------------------------------------------------
    //! @brief      初期化処理を行います.
    //! 
    //! @param[in]      px          位置X成分.
    //! @param[in]      py          位置Y成分.
    //! @note       パッド操作はロックされます.
    //-------------------------------------------------------------------------
    void Init(float px, float py, float sx, float sy, bool center);

    //-------------------------------------------------------------------------
    //! @brief      終了処理を行います.
    //-------------------------------------------------------------------------
    void Term();

    //-------------------------------------------------------------------------
    //! @brief      更新処理を行います.
    //! 
    //! @param[in]      w           画面の横幅.
    //! @param[in]      h           画面の縦幅.
    //-------------------------------------------------------------------------
    void Update(uint32_t w, uint32_t h);

    //-------------------------------------------------------------------------
    //! @brief      ゲームパッドを取得します.
    //! 
    //! @return     ゲームパッドを返却します.
    //-------------------------------------------------------------------------
    const asdx::GamePad& GetPad() const;

    //-------------------------------------------------------------------------
    //! @brief      パッド操作をロックします.
    //! 
    //! @param[in]      value       ロックする場合は true, 解除する場合は false を指定.
    //-------------------------------------------------------------------------
    void SetPadLock(bool value);

    //-------------------------------------------------------------------------
    //! @brief      パッド操作がロックされているかチェックします
    //! 
    //! @retval true    パッド操作ロック中.
    //! @retval false   パッド操作ロック解除中.
    //-------------------------------------------------------------------------
    bool IsPadLock() const;

    //-------------------------------------------------------------------------
    //! @brief      残機数を設定します.
    //! 
    //! @param[in]      value       設定する残機数
    //-------------------------------------------------------------------------
    void SetLife(uint8_t value);

    //-------------------------------------------------------------------------
    //! @brief      残機数を取得します.
    //! 
    //! @return     残機数を返却します.
    //-------------------------------------------------------------------------
    uint8_t GetLife() const;

private:
    //=========================================================================
    // private variables.
    //=========================================================================
    asdx::GamePad   m_Pad;                  //!< ゲームパッド.
    uint8_t         m_Life      = 0;        //!< 残機数.
    float           m_MoveSpeed = 1.0f;     //!< 移動スピード.
    bool            m_PadLock   = false;    //!< パッド操作ロック.

    //=========================================================================
    // private methods.
    //=========================================================================
    /* NOTHING */
};

//-----------------------------------------------------------------------------
//! @brief      プレイヤーを取得します.
//! 
//! @return     プレイヤーを返却します.
//-----------------------------------------------------------------------------
Player& GetPlayer();
