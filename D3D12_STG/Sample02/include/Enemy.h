#include "Entity.h"
#include <unordered_map>
#include <fnd/asdxList.h>


///////////////////////////////////////////////////////////////////////////////
// EnemyParam structure
///////////////////////////////////////////////////////////////////////////////
struct EnemyParam
{
    float   ShotAngle       = 0.0f;     //!< 発射角度.
    float   ShotAngleRange  = 0.0f;     //!< 発射角度範囲.
    float   ShotSpeed       = 0.0f;     //!< 発射速度.
    int     ShotCount       = 0;        //!< 発射数.
    int     ShotInterval    = 0;        //!< 発射間隔.
    int     ShotWait        = 0;        //!< 発射待機時間
    int     ShotTimer       = 0;        //!< 発射用タイマー.
    float   BulletAngleRate = 0.0f;     //!< 弾の角度速度.
    float   BulletSpeedRate = 0.0f;     //!< 弾の加速度.
    int     MoveTimer       = 0;        //!< 移動用タイマー. 
};

//-----------------------------------------------------------------------------
//! @brief      敵の挙動関数.
//! 
//! @param[in, out]     pos         自身の位置座標.
//! @param[in, out]     param       敵パラメータ.
//-----------------------------------------------------------------------------
using EnemyBehavior = void(*)(asdx::Vector2& pos, EnemyParam& param);


///////////////////////////////////////////////////////////////////////////////
// Enemy class
///////////////////////////////////////////////////////////////////////////////
class Enemy : public Entity, public asdx::List<Enemy>::Node
{
    //=========================================================================
    // list of friend classes and methods.
    //=========================================================================
    /* NOTHING */

public:
    //=========================================================================
    // public variables.
    //=========================================================================
    /* NOTHING */

    //=========================================================================
    // public methods.
    //=========================================================================

    //-------------------------------------------------------------------------
    //! @brief      コンストラクタです.
    //-------------------------------------------------------------------------
    Enemy();

    //-------------------------------------------------------------------------
    //! @brief      デストラクタです.
    //-------------------------------------------------------------------------
    ~Enemy();

    //-------------------------------------------------------------------------
    //! @brief      セットアップ処理を行います.
    //! 
    //! @param[in]      kind                スプライト種別.
    //! @param[in]      x                   位置X成分.
    //! @param[in]      y                   位置Y成分.
    //! @param[in]      shotBehavior        発弾挙動です.
    //! @param[in]      moveBehavior        移動挙動です.
    //-------------------------------------------------------------------------
    void Setup(
        uint16_t            kind,
        float               x,
        float               y,
        const EnemyParam&   param,
        EnemyBehavior       shotBehavior,
        EnemyBehavior       moveBehaior);

    //-------------------------------------------------------------------------
    //! @brief      制御パラメータを設定します
    //! 
    //! @param[in]      value       設定するパラメータ.
    //-------------------------------------------------------------------------
    void SetParam(const EnemyParam& value);

    //-------------------------------------------------------------------------
    //! @brief      制御パラメータを取得します.
    //! 
    //! @return     制御パラメータを返却します.
    //-------------------------------------------------------------------------
    const EnemyParam& GetParam() const;

    //-------------------------------------------------------------------------
    //! @brief      発弾挙動を設定します.
    //! 
    //! @param[in]      value       設定する挙動関数.
    //-------------------------------------------------------------------------
    void SetShotBehavior(EnemyBehavior value);

    //-------------------------------------------------------------------------
    //! @brief      移動挙動を設定します.
    //! 
    //! @param[in]      value       設定する挙動関数.
    //-------------------------------------------------------------------------
    void SetMoveBehavior(EnemyBehavior value);

    //-------------------------------------------------------------------------
    //! @brief      更新処理を行います.
    //-------------------------------------------------------------------------
    void Update();

private:
    //=========================================================================
    // private variables.
    //=========================================================================
    EnemyParam      m_Param         = {};
    EnemyBehavior   m_ShotBehavior  = nullptr;
    EnemyBehavior   m_MoveBehavior  = nullptr;

    //=========================================================================
    // private methods.
    //=========================================================================
    /* NOTHING */
};


///////////////////////////////////////////////////////////////////////////////
// EnemyManager class
///////////////////////////////////////////////////////////////////////////////
class EnemyManager
{
    //=========================================================================
    // list of friend classes and methods.
    //=========================================================================
    /* NOTHING */

public:
    ///////////////////////////////////////////////////////////////////////////
    // SpawnParam structure
    ///////////////////////////////////////////////////////////////////////////
    struct SpawnParam
    {
        uint16_t        Kind;           //!< スプライト種別.
        EnemyParam      Param;          //!< 制御パラメータ.
        EnemyBehavior   ShotBehavior;   //!< 発弾挙動.
        EnemyBehavior   MoveBehavior;   //!< 移動挙動.
    };

    //=========================================================================
    // public variables.
    //=========================================================================
    /* NOTHING */

    //=========================================================================
    // public methods.
    //=========================================================================

    //-------------------------------------------------------------------------
    //! @brief      コンストラクタです.
    //-------------------------------------------------------------------------
    EnemyManager();

    //-------------------------------------------------------------------------
    //! @brief      デストラクタです.
    //-------------------------------------------------------------------------
    ~EnemyManager();

    //-------------------------------------------------------------------------
    //! @brief      初期化処理を行います.
    //! 
    //! @param[in]      count       最大敵数.
    //! @retval true    初期化に成功.
    //! @retval false   初期化に失敗.
    //-------------------------------------------------------------------------
    bool Init(uint32_t count);

    //-------------------------------------------------------------------------
    //! @brief      終了処理を行います.
    //-------------------------------------------------------------------------
    void Term();

    //-------------------------------------------------------------------------
    //! @brief      生成タイプを登録します.
    //! 
    //! @param[in]      type        タイプ.
    //! @param[in]      param       生成パラメータ.
    //-------------------------------------------------------------------------
    void AddType(uint32_t type, SpawnParam param);

    //-------------------------------------------------------------------------
    //! @brief      生成タイプを登録解除します.
    //! 
    //! @param[in]      type        タイプ.
    //-------------------------------------------------------------------------
    void DelType(uint32_t type);

    //-------------------------------------------------------------------------
    //! @brief      生成タイプをクリアします.
    //-------------------------------------------------------------------------
    void ClearTypes();

    //-------------------------------------------------------------------------
    //! @brief      敵を生成します.
    //! 
    //! @param[in]      type        敵タイプ.
    //! @param[in]      x           位置X成分.
    //! @param[in]      y           位置Y成分.
    //! @retval true    生成に成功.
    //! @retval false   生成に失敗.
    //-------------------------------------------------------------------------
    bool Spwan(uint32_t type, float x, float py);

    //-------------------------------------------------------------------------
    //! @brief      更新処理を行います.
    //-------------------------------------------------------------------------
    void Update(uint32_t w, uint32_t h);

    //-------------------------------------------------------------------------
    //! @brief      スプライトを描画します.
    //-------------------------------------------------------------------------
    void Draw(asdx::SpriteRenderer& renderer);

    //-------------------------------------------------------------------------
    //! @brief      交差判定を行います.
    //! 
    //! @param[in]      entry       対象エンティティ.
    //! @retval true    交差あり.
    //! @retval false   交差なし.
    //-------------------------------------------------------------------------
    bool IsHit(const Entity& entity);

private:
    //=========================================================================
    // private variables.
    //=========================================================================
    Enemy*              m_Enemies   = nullptr;  //!< 敵.
    uint32_t            m_MaxCount  = 0;        //!< 最大弾数.
    uint32_t            m_UsedCount = 0;        //!< 使用数.
    asdx::List<Enemy>   m_FreeList;             //!< 未使用リスト.
    asdx::List<Enemy>   m_UsedList;             //!< 使用中リスト.

    std::unordered_map<uint32_t, SpawnParam> m_Types;

    //=========================================================================
    // private methods.
    //=========================================================================
    /* NOTHING */
};

//-----------------------------------------------------------------------------
//! @brief      敵マネージャを取得します.
//-----------------------------------------------------------------------------
EnemyManager& GetEnemyManager();
