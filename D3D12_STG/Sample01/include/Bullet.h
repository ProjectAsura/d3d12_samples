///////////////////////////////////////////////////////////////////////////////
// Bullet class
///////////////////////////////////////////////////////////////////////////////
class Bullet : public Entity, public asdx::List<Bullet>::Node
{
    //=========================================================================
    // list of friend classes and methods.
    //=========================================================================
    /* NOTHING */

public:
    //=========================================================================
    // public variables.
    //=========================================================================
    /* NOTHING */

    //=========================================================================
    // public methods.
    //=========================================================================

    //-------------------------------------------------------------------------
    //! @brief      コンストラクタです.
    //-------------------------------------------------------------------------
    Bullet();

    //-------------------------------------------------------------------------
    //! @brief      引数付きコンストラクタです.
    //! 
    //! @param[in]      kind        スプライト種別.
    //! @param[in]      px          位置X成分.
    //! @param[in]      py          位置Y成分.
    //! @param[in]      angle       角度.
    //! @param[in]      angleRate   角度加算値.
    //! @param[in]      speed       速さ.
    //! @param[in]      speedRate   速さ調整値.
    //-------------------------------------------------------------------------
    Bullet(
        uint16_t kind,
        float    px,
        float    py,
        float    angle,
        float    angleRate,
        float    speed,
        float    speedRate);

    //-------------------------------------------------------------------------
    //! @brief      角度を設定します.
    //-------------------------------------------------------------------------
    void SetAngle(float value)
    { m_Angle = value; }

    //-------------------------------------------------------------------------
    //! @brief      角度加算値を設定します.
    //-------------------------------------------------------------------------
    void SetAngleRate(float value)
    { m_AngleRate = value; }

    //-------------------------------------------------------------------------
    //! @brief      速度を設定します.
    //-------------------------------------------------------------------------
    void SetSpeed(float value)
    { m_Speed = value; }

    //-------------------------------------------------------------------------
    //! @brief      速度加算値を設定します.
    //-------------------------------------------------------------------------
    void SetSpeedRate(float value)
    { m_SpeedRate = value; }

    //-------------------------------------------------------------------------
    //! @brief      角度を取得します.
    //-------------------------------------------------------------------------
    float GetAngle() const
    { return m_Angle; }

    //-------------------------------------------------------------------------
    //! @brief      角度加算値を取得します.
    //-------------------------------------------------------------------------
    float GetAngleRate() const
    { return m_AngleRate; }

    //-------------------------------------------------------------------------
    //! @brief      速度を取得します.
    //-------------------------------------------------------------------------
    float GetSpeed() const
    { return m_Speed; }

    //-------------------------------------------------------------------------
    //! @brief      速度加算値を取得します.
    //-------------------------------------------------------------------------
    float GetSpeedRate() const
    { return m_SpeedRate; }

    //-------------------------------------------------------------------------
    //! @brief      更新処理を行います.
    //-------------------------------------------------------------------------
    void Update();

private:
    //=========================================================================
    // private variables.
    //=========================================================================
    float   m_Angle     = 0.0f; //!< 角度.
    float   m_AngleRate = 0.0f; //!< 角度加算値.
    float   m_Speed     = 0.0f; //!< 速度.
    float   m_SpeedRate = 0.0f; //!< 速度加算値.

    //=========================================================================
    // private methods.
    //=========================================================================
    /* NOTHING */
};


///////////////////////////////////////////////////////////////////////////////
// BulletManager class
///////////////////////////////////////////////////////////////////////////////
class BulletManager
{
    //=========================================================================
    // list of friend classes and methods.
    //=========================================================================
    /* NOTHING */

public:
    //=========================================================================
    // public variables.
    //=========================================================================
    /* NOTHING */

    //=========================================================================
    // public methods.
    //=========================================================================

    //-------------------------------------------------------------------------
    //! @brief      コンストラクタです.
    //-------------------------------------------------------------------------
    BulletManager();

    //-------------------------------------------------------------------------
    //! @brief      デストラクタです.
    //-------------------------------------------------------------------------
    ~BulletManager();

    //-------------------------------------------------------------------------
    //! @brief      初期化処理を行います
    //! 
    //! @param[in]      count       最大弾数.
    //! @retval true    初期化に成功.
    //! @retval fasle   初期化に失敗.
    //-------------------------------------------------------------------------
    bool Init(uint32_t count);

    //-------------------------------------------------------------------------
    //! @brief      終了処理を行います.
    //-------------------------------------------------------------------------
    void Term();

    //-------------------------------------------------------------------------
    //! @brief      弾を生成します.
    //! 
    //! @param[in]      kind        スプライト種別.
    //! @param[in]      px          位置X成分.
    //! @param[in]      py          位置Y成分.
    //! @param[in]      angle       角度.
    //! @param[in]      angleRate   角度加算値.
    //! @param[in]      speed       速さ.
    //! @param[in]      speedRate   速さ加算値.
    //! @retval true    生成に成功.
    //! @retval false   生成に失敗.
    //-------------------------------------------------------------------------
    bool Spwan(
        uint16_t kind,
        float    px,
        float    py,
        float    arngle,
        float    angleRate,
        float    speed,
        float    speedRate);

    //-------------------------------------------------------------------------
    //! @brief      更新処理を行います.
    //! 
    //! @param[in]      w       画面の横幅.
    //! @param[in]      h       画面の縦幅.
    //-------------------------------------------------------------------------
    void Update(uint32_t w, uint32_t h);

    //-------------------------------------------------------------------------
    //! @brief      スプライトを描画します.
    //! 
    //! @param[in]      renderer    スプライトレンダラー.
    //-------------------------------------------------------------------------
    void Draw(asdx::SpriteRenderer& renderer);

    //-------------------------------------------------------------------------
    //! @brief      交差判定を行います.
    //! 
    //! @param[in]      entry       対象エンティティ.
    //! @retval true    交差あり.
    //! @retval false   交差なし.
    //-------------------------------------------------------------------------
    bool IsHit(const Entity& entity);

private:
    //=========================================================================
    // private variables.
    //=========================================================================
    Bullet*             m_Bullets   = nullptr;  //!< 弾.
    uint32_t            m_MaxCount  = 0;        //!< 最大弾数.
    uint32_t            m_UsedCount = 0;        //!< 使用数.
    asdx::List<Bullet>  m_FreeList;             //!< 未使用リスト.
    asdx::List<Bullet>  m_UsedList;             //!< 使用中リスト.

    //=========================================================================
    // private methods.
    //=========================================================================
    /* NOTHING */
};