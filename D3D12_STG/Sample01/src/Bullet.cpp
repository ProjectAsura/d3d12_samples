#include "Bullet.h"


///////////////////////////////////////////////////////////////////////////////
// Bullet class
///////////////////////////////////////////////////////////////////////////////

//-----------------------------------------------------------------------------
//      コンストラクタです.
//-----------------------------------------------------------------------------
Bullet::Bullet()
: Entity()
{ /* DO_NOTHING */ }

//-----------------------------------------------------------------------------
//      引数付きコンストラクタです.
//-----------------------------------------------------------------------------
Bullet::Bullet(uint16_t kind, float px, float py, float vx, float vy)
: Entity(kind, px, py)
, m_Vel(vx, vy)
{ /* DO_NOTHING */ }

//-----------------------------------------------------------------------------
//      引数付きコンストラクタです.
//-----------------------------------------------------------------------------
Bullet::Bullet(uint16_t kind, const asdx::Vector2& pos, const asdx::Vector2& vel)
: Entity(kind, pos)
, m_Vel(vel)
{ /* DO_NOTHING */ }

//-----------------------------------------------------------------------------
//      速度ベクトルを設定します.
//-----------------------------------------------------------------------------
void Bullet::SetVel(const asdx::Vector2& value)
{ m_Vel = value; }

//-----------------------------------------------------------------------------
//      速度ベクトルを設定します.
//-----------------------------------------------------------------------------
void Bullet::SetVel(float x, float y)
{
    m_Vel.x = x;
    m_Vel.y = y;
}

//-----------------------------------------------------------------------------
//      速度ベクトルのX成分を設定します.
//-----------------------------------------------------------------------------
void Bullet::SetVelX(float value)
{ m_Vel.x = value; }

//-----------------------------------------------------------------------------
//      速度ベクトルのY成分を設定します.
//-----------------------------------------------------------------------------
void Bullet::SetVelY(float value)
{ m_Vel.y = value; }

//-----------------------------------------------------------------------------
//      速度ベクトルを取得します.
//-----------------------------------------------------------------------------
const asdx::Vector2& Bullet::GetVel() const
{ return m_Vel; }

//-----------------------------------------------------------------------------
//      速度ベクトルのX成分を取得します.
//-----------------------------------------------------------------------------
float Bullet::GetVelX() const
{ return m_Vel.x; }

//-----------------------------------------------------------------------------
//      速度ベクトルのY成分を取得します.
//-----------------------------------------------------------------------------
float Bullet::GetVelY() const
{ return m_Vel.y; }

//-----------------------------------------------------------------------------
//      更新処理を行います.
//-----------------------------------------------------------------------------
void Bullet::Update()
{
    asdx::Vector2 pos = GetPos();
    pos += m_Vel;
    SetPos(pos);
}
