//-----------------------------------------------------------------------------
//      方向弾.
//-----------------------------------------------------------------------------
void DirectionalShot(Enemy& entity)
{
    auto& param = entity.GetShotParam();
    auto& pos = entity.GetPos();
    GetEnemyBulletMgr().Spwan(param.Kind, pos.x, pos.y, param.Angle, 0.0f, param.Speed, 0.0f);
}

