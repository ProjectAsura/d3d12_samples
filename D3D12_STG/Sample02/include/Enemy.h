#include "Entity.h"
#include <unordered_map>
#include <fnd/asdxList.h>


//-----------------------------------------------------------------------------
// Forward Declarations.
//-----------------------------------------------------------------------------
class Enemy;
using EnemyBehavior = void(*)(Enemy&);  // 挙動処理.


///////////////////////////////////////////////////////////////////////////////
// Enemy class
///////////////////////////////////////////////////////////////////////////////
class Enemy : public Entity, public asdx::List<Enemy>::Node
{
    //=========================================================================
    // list of friend classes and methods.
    //=========================================================================
    /* NOTHING */

public:
    ///////////////////////////////////////////////////////////////////////////
    // ShotParam structure
    ///////////////////////////////////////////////////////////////////////////
    struct ShotParam
    {
        uint16_t    Kind            = 0;        //!< スプライト種別.
        float       Angle           = 0.0f;     //!< 発射角度.
        float       AngleRange      = 0.0f;     //!< 発射角度範囲.
        float       AngleRate       = 0.0f;     //!< 弾の角度速度.
        float       Speed           = 0.0f;     //!< 発射速度.
        float       SpeedRate       = 0.0f;     //!< 弾の加速度.
        int         Count           = 0;        //!< 発射数.
        int         Interval        = 0;        //!< 発射間隔.
        int         Wait            = 0;        //!< 発射待機時間
        int         Timer           = 0;        //!< タイマー.
    };

    ///////////////////////////////////////////////////////////////////////////
    // MoveParam structure
    ///////////////////////////////////////////////////////////////////////////
    struct MoveParam
    {
        int Timer = 0;  //!< タイマー. 
    };

    //=========================================================================
    // public variables.
    //=========================================================================
    /* NOTHING */

    //=========================================================================
    // public methods.
    //=========================================================================

    //-------------------------------------------------------------------------
    //! @brief      コンストラクタです.
    //-------------------------------------------------------------------------
    Enemy();

    //-------------------------------------------------------------------------
    //! @brief      デストラクタです.
    //-------------------------------------------------------------------------
    ~Enemy();

    //-------------------------------------------------------------------------
    //! @brief      セットアップ処理を行います.
    //! 
    //! @param[in]      kind                スプライト種別.
    //! @param[in]      x                   位置X成分.
    //! @param[in]      y                   位置Y成分.
    //! @param[in]      shotBehavior        発弾挙動です.
    //! @param[in]      moveBehavior        移動挙動です.
    //-------------------------------------------------------------------------
    void Setup(
        uint16_t            kind,
        float               x,
        float               y,
        const ShotParam&    shotParam,
        const MoveParam&    moveParam,
        EnemyBehavior       shotBehavior,
        EnemyBehavior       moveBehaior);

    //-------------------------------------------------------------------------
    //! @brief      発弾パラメータを設定します
    //! 
    //! @param[in]      value       設定するパラメータ.
    //-------------------------------------------------------------------------
    void SetShotParam(const ShotParam& value);

    //-------------------------------------------------------------------------
    //! @brief      移動パラメータを設定します.
    //! 
    //! @param[in]      value       設定するパラメータ.
    //-------------------------------------------------------------------------
    void SetMoveParam(const MoveParam& value);

    //-------------------------------------------------------------------------
    //! @brief      発弾パラメータを取得します.
    //! 
    //! @return     発弾パラメータを返却します.
    //-------------------------------------------------------------------------
    const ShotParam& GetShotParam() const;

    //-------------------------------------------------------------------------
    //! @brief      移動パラメータを取得します.
    //! 
    //! @return     移動パラメータを返却します.
    //-------------------------------------------------------------------------
    const MoveParam& GetMoveParam() const;

    //-------------------------------------------------------------------------
    //! @brief      発弾挙動を設定します.
    //! 
    //! @param[in]      value       設定する挙動関数.
    //-------------------------------------------------------------------------
    void SetShotBehavior(EnemyBehavior value);

    //-------------------------------------------------------------------------
    //! @brief      移動挙動を設定します.
    //! 
    //! @param[in]      value       設定する挙動関数.
    //-------------------------------------------------------------------------
    void SetMoveBehavior(EnemyBehavior value);

    //-------------------------------------------------------------------------
    //! @brief      更新処理を行います.
    //-------------------------------------------------------------------------
    void Update();

private:
    //=========================================================================
    // private variables.
    //=========================================================================
    ShotParam       m_ShotParam     = {};
    MoveParam       m_MoveParam     = {};
    EnemyBehavior   m_ShotBehavior  = nullptr;
    EnemyBehavior   m_MoveBehavior  = nullptr;

    //=========================================================================
    // private methods.
    //=========================================================================
    /* NOTHING */
};


///////////////////////////////////////////////////////////////////////////////
// EnemyManager class
///////////////////////////////////////////////////////////////////////////////
class EnemyManager
{
    //=========================================================================
    // list of friend classes and methods.
    //=========================================================================
    /* NOTHING */

public:
    ///////////////////////////////////////////////////////////////////////////
    // SpawnParam structure
    ///////////////////////////////////////////////////////////////////////////
    struct SpawnParam
    {
        uint16_t            ShipKind;       //!< スプライト種別.
        Enemy::ShotParam    InitShotParam;  //!< 発弾初期パラメータ.
        Enemy::MoveParam    InitMoveParam;  //!< 移動初期パラメータ.
        EnemyBehavior       ShotBehavior;   //!< 発弾挙動.
        EnemyBehavior       MoveBehavior;   //!< 移動挙動.
    };

    //=========================================================================
    // public variables.
    //=========================================================================
    /* NOTHING */

    //=========================================================================
    // public methods.
    //=========================================================================

    //-------------------------------------------------------------------------
    //! @brief      コンストラクタです.
    //-------------------------------------------------------------------------
    EnemyManager();

    //-------------------------------------------------------------------------
    //! @brief      デストラクタです.
    //-------------------------------------------------------------------------
    ~EnemyManager();

    //-------------------------------------------------------------------------
    //! @brief      初期化処理を行います.
    //! 
    //! @param[in]      count       最大敵数.
    //! @retval true    初期化に成功.
    //! @retval false   初期化に失敗.
    //-------------------------------------------------------------------------
    bool Init(uint32_t count);

    //-------------------------------------------------------------------------
    //! @brief      終了処理を行います.
    //-------------------------------------------------------------------------
    void Term();

    //-------------------------------------------------------------------------
    //! @brief      生成タイプを登録します.
    //! 
    //! @param[in]      type        タイプ.
    //! @param[in]      param       生成パラメータ.
    //-------------------------------------------------------------------------
    void AddType(uint32_t type, SpawnParam param);

    //-------------------------------------------------------------------------
    //! @brief      生成タイプを登録解除します.
    //! 
    //! @param[in]      type        タイプ.
    //-------------------------------------------------------------------------
    void DelType(uint32_t type);

    //-------------------------------------------------------------------------
    //! @brief      生成タイプをクリアします.
    //-------------------------------------------------------------------------
    void ClearTypes();

    //-------------------------------------------------------------------------
    //! @brief      敵を生成します.
    //! 
    //! @param[in]      type        敵タイプ.
    //! @param[in]      x           位置X成分.
    //! @param[in]      y           位置Y成分.
    //! @retval true    生成に成功.
    //! @retval false   生成に失敗.
    //-------------------------------------------------------------------------
    bool Spwan(uint32_t type, float x, float py);

    //-------------------------------------------------------------------------
    //! @brief      更新処理を行います.
    //-------------------------------------------------------------------------
    void Update(uint32_t w, uint32_t h);

    //-------------------------------------------------------------------------
    //! @brief      スプライトを描画します.
    //-------------------------------------------------------------------------
    void Draw(asdx::SpriteRenderer& renderer);

    //-------------------------------------------------------------------------
    //! @brief      交差判定を行います.
    //! 
    //! @param[in]      entry       対象エンティティ.
    //! @retval true    交差あり.
    //! @retval false   交差なし.
    //-------------------------------------------------------------------------
    bool IsHit(const Entity& entity);

private:
    //=========================================================================
    // private variables.
    //=========================================================================
    Enemy*              m_Enemies   = nullptr;  //!< 敵.
    uint32_t            m_MaxCount  = 0;        //!< 最大弾数.
    uint32_t            m_UsedCount = 0;        //!< 使用数.
    asdx::List<Enemy>   m_FreeList;             //!< 未使用リスト.
    asdx::List<Enemy>   m_UsedList;             //!< 使用中リスト.

    std::unordered_map<uint32_t, SpawnParam> m_Types;   //!< 敵タイプリスト.

    //=========================================================================
    // private methods.
    //=========================================================================
    /* NOTHING */
};

//-----------------------------------------------------------------------------
//! @brief      敵マネージャを取得します.
//-----------------------------------------------------------------------------
EnemyManager& GetEnemyMgr();
